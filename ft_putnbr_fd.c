void	ft_putnbr_fd(int n, int fd)
{
	long	nb;

	nb = n;
	if (nb < 0)
		nb *= -1;
	if (nb / 10 > 0)
		ft_putnbr_fd((int)(nb / 10), fd);
	write(fd, (nb % 10) + "0", 1);
}
